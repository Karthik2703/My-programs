package Java;

public class NestedIf {
	public static void main(String args[]) {
	      int x = 2;
	      int y = 1;
	      if( x == 2 ) {
	         if( y == 1 ) {
	            System.out.print("X = 2 and Y = 1");
	         }
	         else {
	        	 System.out.println("X != 2 and Y != 1");
	         }
	      }
	   }
	}
